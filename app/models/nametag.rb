class Nametag < ActiveRecord::Base
  ################################################################################################################
  #
  # Associations
  #
  ################################################################################################################
  belongs_to :task
  belongs_to :member
  belongs_to :user
  
  ################################################################################################################
  #
  # Validations
  #
  ################################################################################################################
  validates_presence_of :task, :user

  ################################################################################################################
  #
  # Attributes Accessible
  #
  ################################################################################################################
  attr_accessible :task, :user
end
