class Statustag < ActiveRecord::Base
  ################################################################################################################
  #
  # Associations
  #
  ################################################################################################################
  belongs_to :task
  
  ################################################################################################################
  #
  # Validations
  #
  ################################################################################################################
  validates_presence_of :task, :status
  
  ################################################################################################################
  #
  # Attributes Accessible
  #
  ################################################################################################################
  attr_accessible :task, :status, :relative_position_x, :relative_position_y
end
